package NaveenPrograms;

public class TableOfMultiplication {

    public static void main(String[] args) {
        int num=6;

        for(int i=1;i<=10;i++){
           int res=num*i;
          System.out.println(num+"*"+i+"="+res);
       //     System.out.println(num+"x"+i+"="+(num*i));
        }


    }
}